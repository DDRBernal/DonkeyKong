package aplicacion;

public class BarrilA {

    protected double posX;
    protected double posY;



    public BarrilA(){
        posX=100;
        posY=100;
    }

    public void setPosX(double x) {
        posX = x;
    }

    public void setPosY(double y) {
        posY = y;
    }

    public double getX() {
        return posX;
    }

    public double getY() {
        return posY;
    }






}
