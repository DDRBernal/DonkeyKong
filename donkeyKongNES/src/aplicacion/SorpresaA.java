package aplicacion;

public class SorpresaA {

    private double posX,posY;

    public SorpresaA(double posX, double posY) {
        this.posX = posX;
        this.posY = posY;
    }

    public double getX() {
        return posX;
    }

    public double getY(){
        return posY;
    }
}
